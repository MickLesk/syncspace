// Translation helper function
export function t(lang, key, ...args) {
  let text = translations[lang]?.[key] || translations['en'][key] || key;
  
  // Replace placeholders {0}, {1}, etc.
  if (args.length > 0) {
    args.forEach((arg, index) => {
      text = text.replace(`{${index}}`, arg);
    });
  }
  
  return text;
}

// Format relative time
export function formatRelativeTime(lang, date) {
  const now = new Date();
  const then = new Date(date);
  const diff = now.getTime() - then.getTime();
  const seconds = Math.floor(diff / 1000);
  const minutes = Math.floor(seconds / 60);
  const hours = Math.floor(minutes / 60);
  const days = Math.floor(hours / 24);
  const weeks = Math.floor(days / 7);
  const months = Math.floor(days / 30);
  const years = Math.floor(days / 365);

  if (seconds < 60) return t(lang, 'justNow');
  if (minutes < 60) return t(lang, 'minutesAgo', minutes);
  if (hours < 24) return t(lang, 'hoursAgo', hours);
  if (days < 7) return t(lang, 'daysAgo', days);
  if (weeks < 4) return t(lang, 'weeksAgo', weeks);
  if (months < 12) return t(lang, 'monthsAgo', months);
  return t(lang, 'yearsAgo', years);
}

// Format file size with locale
export function formatFileSize(bytes, lang = 'en') {
  if (bytes === 0) return '0 B';
  const k = 1024;
  const sizes = ['B', 'KB', 'MB', 'GB', 'TB'];
  const i = Math.floor(Math.log(bytes) / Math.log(k));
  const value = (bytes / Math.pow(k, i)).toFixed(2);
  
  // Use locale-specific decimal separator
  const decimal = lang === 'de' ? ',' : '.';
  const formatted = value.replace('.', decimal);
  
  return `${formatted} ${sizes[i]}`;
}